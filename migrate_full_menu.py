async def migrate_submenu(menu_id, menu_data, parent_id=None, full_path=""):
    """–†–µ–∫—É—Ä—Å–∏–≤–Ω–æ –º–∏–≥—Ä–∏—Ä—É–µ—Ç –ø–æ–¥–º–µ–Ω—é –≤ –ë–î"""
    # –û–ø—Ä–µ–¥–µ–ª—è–µ–º —Ç–µ–∫—Å—Ç –∫–æ–Ω—Ç–µ–Ω—Ç–∞ –∏ pages
    pages_json = None
    if 'pages' in menu_data and menu_data['pages']:
        # –ú–Ω–æ–≥–æ—Å—Ç—Ä–∞–Ω–∏—á–Ω–æ–µ –º–µ–Ω—é - –±–µ—Ä—ë–º –ø–µ—Ä–≤—É—é —Å—Ç—Ä–∞–Ω–∏—Ü—É –¥–ª—è content, –≤—Å–µ –¥–ª—è pages_json
        text_content = menu_data['pages'][0].get('text', '')
        # –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤—Å–µ —Å—Ç—Ä–∞–Ω–∏—Ü—ã –≤ JSON
        pages_json = json.dumps([{'text': page.get('text', '')} for page in menu_data['pages']])
    else:
        text_content = menu_data.get('text', '')

    # –°–æ–±–∏—Ä–∞–µ–º –∏–Ω–ª–∞–π–Ω-–∫–Ω–æ–ø–∫–∏
    buttons = []

    # –ö–Ω–æ–ø–∫–∏ –∏–∑ submenu
    if menu_data.get('type') == 'inline' and menu_data.get('submenu'):
        for submenu_key, submenu_data in menu_data['submenu'].items():
            if isinstance(submenu_data, dict):
                # –°–æ–∑–¥–∞—ë–º —É–Ω–∏–∫–∞–ª—å–Ω—ã–π ID: –í–°–ï–ì–î–ê parent:child –¥–ª—è consistency
                submenu_label = submenu_data.get('label', submenu_key)
                submenu_full_id = f"{menu_id}:{submenu_label}"

                # –û–±—ã—á–Ω–æ–µ –ø–æ–¥–º–µ–Ω—é
                buttons.append({
                    'text': submenu_label,
                    'id': submenu_full_id
                })
            else:
                print(f"[WARNING] Unexpected submenu data for {submenu_key}: {submenu_data}")

    # –ö–Ω–æ–ø–∫–∏ –∏–∑ buttons –º–∞—Å—Å–∏–≤–∞
    if 'buttons' in menu_data:
        for btn in menu_data['buttons']:
            if btn.get('url'):
                buttons.append({
                    'text': btn['text'],
                    'url': btn['url']
                })
            elif btn.get('callback'):
                # –ö–Ω–æ–ø–∫–∏ —Å callback (–Ω–∞–ø—Ä–∏–º–µ—Ä "–ù–∞–∑–∞–¥")
                buttons.append({
                    'text': btn['text'],
                    'url': '–º–µ–Ω—é'  # –°–ø–µ—Ü–∏–∞–ª—å–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –¥–ª—è –∫–Ω–æ–ø–∫–∏ –Ω–∞–∑–∞–¥
                })

    # –ï—Å–ª–∏ –µ—Å—Ç—å URL –≤ —Å–∞–º–æ–º –º–µ–Ω—é (–¥–ª—è –∫–Ω–æ–ø–æ–∫-—Å—Å—ã–ª–æ–∫)
    if menu_data.get('url'):
        # –≠—Ç–æ –∫–Ω–æ–ø–∫–∞-—Å—Å—ã–ª–∫–∞, –Ω–µ —Å–æ–∑–¥–∞—ë–º –∫–æ–Ω—Ç–µ–Ω—Ç
        print(f"[INFO] Skipping URL button: {menu_id}")
        return

    # –°–æ—Ö—Ä–∞–Ω—è–µ–º –∫–æ–Ω—Ç–µ–Ω—Ç
    buttons_json = json.dumps(buttons) if buttons else None

    success = await update_button_content(
        menu_id,
        text_content,
        None,  # photo_file_id
        buttons_json,
        'HTML',
        parent_id,
        None,  # buttons_per_row
        pages_json  # pages_json
    )

    if success:
        print(f"   ‚úÖ Migrated: {menu_id}")
    else:
        print(f"   ‚ùå Failed: {menu_id}")

    # –†–µ–∫—É—Ä—Å–∏–≤–Ω–æ –º–∏–≥—Ä–∏—Ä—É–µ–º –≤–ª–æ–∂–µ–Ω–Ω—ã–µ –ø–æ–¥–º–µ–Ω—é
    if menu_data.get('submenu'):
        for submenu_key, submenu_data in menu_data['submenu'].items():
            if isinstance(submenu_data, dict) and not submenu_data.get('url'):
                # –°–æ–∑–¥–∞—ë–º –ø–æ–ª–Ω—ã–π –ø—É—Ç—å –¥–ª—è –≤–ª–æ–∂–µ–Ω–Ω–æ–≥–æ –º–µ–Ω—é: –í–°–ï–ì–î–ê parent:child
                submenu_label = submenu_data.get('label', submenu_key)
                submenu_full_id = f"{menu_id}:{submenu_label}"
                new_full_path = f"{full_path}/{submenu_key}" if full_path else submenu_key

                await migrate_submenu(
                    submenu_full_id,
                    submenu_data,
                    parent_id=menu_id,
                    full_path=new_full_path
                )

